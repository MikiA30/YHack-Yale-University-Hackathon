STATE_ALPHA_TO_NAME = {value: key for key, value in STATE_NAME_TO_ALPHA.items()}


def to_state_alpha(state_name: Optional[str]) -> Optional[str]:
    if not state_name:
        return None
    if state_name in STATE_ALPHA_TO_NAME:
        return state_name
    return STATE_NAME_TO_ALPHA.get(state_name)


